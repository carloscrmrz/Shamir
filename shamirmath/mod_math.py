def extended_gdc(a, b):
    x = 0
    last_x = 1
    y = 1
    last_y = 0
    while b != 0:
        quot = a // b
        a, b = b, a % b
        x, last_x = last_x - quot * x, x
        y, last_y = last_y - quot * y, y
    return last_x, last_y


def modular_division(a, b, prime):
    inverse, _ = extended_gdc(b, prime)
    return a * inverse
